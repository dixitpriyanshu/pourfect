CREATE TABLE alcohols (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    name TEXT NOT NULL,
    image_url TEXT
);

CREATE TABLE cocktails (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    name TEXT NOT NULL,
    image_url TEXT,
    instructions TEXT,
    glass_type TEXT,
    alcohol_ids UUID[] -- optional array of alcohol UUIDs
);

CREATE TABLE cocktail_alcohols (
    cocktail_id UUID NOT NULL REFERENCES cocktails(id) ON DELETE CASCADE,
    alcohol_id UUID NOT NULL REFERENCES alcohols(id) ON DELETE CASCADE,
    PRIMARY KEY (cocktail_id, alcohol_id)
);

CREATE TABLE ingredients (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    name TEXT NOT NULL
);

CREATE TABLE cocktail_ingredients (
    cocktail_id UUID NOT NULL REFERENCES cocktails(id) ON DELETE CASCADE,
    ingredient_id UUID NOT NULL REFERENCES ingredients(id) ON DELETE CASCADE,
    measure TEXT,
    PRIMARY KEY (cocktail_id, ingredient_id)
);


